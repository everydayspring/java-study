package week02.Operator;

public class W08 {
    public static void main(String[] args) {
        short x = 10;
        int y = 20;

        int z = x + y;

        long lx = 30L;
        long lz = z + lx;

        float fx = x;
        float fz = y;
        float fy = y;

        System.out.println(lz);
        System.out.println(fx);
        System.out.println(fz);
        System.out.println(fy);
    }
}
