package week02.Operator;

public class W10 {
    public static void main(String[] args) {

        boolean flag = true;

        if (flag) {
            System.out.println("true");
        } else {
            System.out.println("false");
        }

        int num = 2;
        if (num == 1) {
            System.out.println(1);
        } else if (num == 2) {
            System.out.println(2);
        } else {
            System.out.println(0);
        }
    }
}
