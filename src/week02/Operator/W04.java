package week02.Operator;

public class W04 {
    public static void main(String[] args) {

        int num  = 10;
        num = num + 2;
        System.out.println(num);

        num = num - 2;
        System.out.println(num);

        num = num * 2;
        System.out.println(num);

        num = num / 2;
        System.out.println(num);

        num = num % 2;
        System.out.println(num);

        num = 10;

        num += 2;
        System.out.println(num);

        num -= 2;
        System.out.println(num);

        num *= 2;
        System.out.println(num);

        num /= 2;
        System.out.println(num);

        num %= 2;
        System.out.println(num);

        num++;
        System.out.println(num);

        num--;
        System.out.println(num);


    }
}
